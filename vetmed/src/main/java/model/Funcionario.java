package model;

public class Funcionario {
	
	private int idFuncionario;
	private String nomeFuncionario;
	private int cpfFuncionario;
	private String dataNascFuncionario;
	private float salario;
	private String tipo;
	private String cargo;
	private String crm;
	
	public int getIdFuncionario() {
		return idFuncionario;
	}
	public void setIdFuncionario(int idFuncionario) {
		this.idFuncionario = idFuncionario;
	}
	public String getNomeFuncionario() {
		return nomeFuncionario;
	}
	public void setNomeFuncionario(String nomeFuncionario) {
		this.nomeFuncionario = nomeFuncionario;
	}
	public int getCpfFuncionario() {
		return cpfFuncionario;
	}
	public void setCpfFuncionario(int cpfFuncionario) {
		this.cpfFuncionario = cpfFuncionario;
	}
	public String getDataNascFuncionario() {
		return dataNascFuncionario;
	}
	public void setDataNascFuncionario(String dataNascFuncionario) {
		this.dataNascFuncionario = dataNascFuncionario;
	}
	public float getSalario() {
		return salario;
	}
	public void setSalario(float salario) {
		this.salario = salario;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getCargo() {
		return cargo;
	}
	public void setCargo(String cargo) {
		this.cargo = cargo;
	}
	public String getCrm() {
		return crm;
	}
	public void setCrm(String crm) {
		this.crm = crm;
	}	

}
